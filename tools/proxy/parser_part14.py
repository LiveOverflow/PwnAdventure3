def parse(data, port, origin):
    if port==3333:
        return

    while len(data)>=2:
        packet_id = struct.unpack(">H", data[0:2])[0]
        if packet_id not in handlers:
            
            data = data[1:]
        else:
            data = handlers.get(packet_id, h_noop)(data[2:], origin)

